public class Rabbit extends Animal {
    String name,color;

    public Rabbit(String name, String color, boolean veg, String food, int legs) {
        super(veg, food, legs);
        this.name = name;
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public String getName() {
        return name;
    }
    
    
    
}
